export const BODY_CONFIG_BY_ID = new Map(
  BODY_CONFIGS.map((config) => [config.id, config])
);

export const SELECTABLE_BODY_IDS = BODY_CONFIGS.map((config) => config.id);
export const BODIES = BODY_CONFIGS;
export const BODY_MAP = BODY_CONFIG_BY_ID;
export const PLANETS = BODY_CONFIGS.filter((config) => config.type === 'planet');
export const MOONS = BODY_CONFIGS.filter((config) => config.type === 'moon');
export const BELTS = BODY_CONFIGS.filter((config) => config.type === 'belt');
export const FOCUSABLE_BODIES = BODY_CONFIGS.filter((config) => config.focusDistanceMultiplier !== undefined);
export const SELECTABLE_BODIES = BODY_CONFIGS.map((config) => ({
  id: config.id,
  label: config.selectLabel,
  type: config.type,
  parentId: config.parentId ?? null
}));

export function getBody(id) {
  return BODY_CONFIG_BY_ID.get(id) ?? null;
}

export function getChildren(parentId) {
  return BODY_CONFIGS.filter((config) => config.parentId === parentId);
}
