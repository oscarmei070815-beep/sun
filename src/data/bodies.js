import { BODY_DESCRIPTIONS } from './descriptions.js';

function body({
  id,
  name,
  type,
  typeLabel,
  appearance,
  parentId = null,
  displayRadius,
  orbitRadius,
  orbitSpeed,
  rotationSpeed,
  axialTilt = 0,
  orbitInclination = 0,
  orbitPhase = 0,
  color = '#ffffff',
  focusDistanceMultiplier,
  focusTargetOffset,
  focusViewDirection,
  ring,
  hasClouds = false,
  hasAtmosphere = false,
  hasNightLights = false
}) {
  return {
    id,
    name,
    label: name,
    type,
    typeLabel,
    selectLabel: `${typeLabel} · ${name}`,
    appearance,
    parentId,
    displayRadius,
    orbitRadius,
    orbitSpeed,
    rotationSpeed,
    axialTilt,
    orbitInclination,
    orbitPhase,
    color,
    focusDistanceMultiplier,
    focusTargetOffset,
    focusViewDirection,
    ring,
    hasClouds,
    hasAtmosphere,
    hasNightLights,
    description: BODY_DESCRIPTIONS[id]
  };
}

